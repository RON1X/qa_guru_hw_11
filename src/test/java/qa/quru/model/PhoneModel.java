package qa.quru.model;

public class PhoneModel {

    String manufacturer;
    String model;
    int year;
    double price;

    public String getManufacturer() {
        return manufacturer;
    }

    public String getModel() {
        return model;
    }

    public int getYear() {
        return year;
    }

    public double getPrice() {
        return price;
    }
}
